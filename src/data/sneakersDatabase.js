// Export function to get all sneakers or filter by criteria
export const getSneakers = (filter = null) => {
  if (!filter) return sneakersData;
  
  if (filter.brand) {
    return sneakersData.filter(shoe => shoe.brand === filter.brand);
  }
  
  if (filter.category) {
    return sneakersData.filter(shoe => shoe.category === filter.category);
  }
  
  if (filter.priceRange) {
    const [min, max] = filter.priceRange;
    return sneakersData.filter(shoe => shoe.price >= min && shoe.price <= max);
  }
  
  return sneakersData;
};

// Get unique brands
export const getBrands = () => {
  return [...new Set(sneakersData.map(shoe => shoe.brand))].sort();
};

// Get unique categories
export const getCategories = () => {
  return [...new Set(sneakersData.map(shoe => shoe.category))].sort();
};

// Get shoes by rating
export const getShoesByRating = (minRating = 4) => {
  return sneakersData.filter(shoe => shoe.rating >= minRating).sort((a, b) => b.rating - a.rating);
};
